#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)malloc(sizeof(char) * ft_strlen((char *)s) + 1);
	if (!str)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}

static char	ft_mayus(unsigned int i, char c)
{
	return (c - 32);
}


int main (void)
{
	printf("%s\n", ft_strmapi("Barcelona", ft_mayus));
	return (0);
}


